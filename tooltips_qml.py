class TooltipsProvider:
    """
    Класс для предоставления подсказок QML.
    """
    
    @staticmethod
    def get_tooltip(key: str) -> str:
        """
        Получает подсказку по ключу.
        :param key: Ключ подсказки
        :return: Текст подсказки или пустая строка
        """
        return TOOLTIPS.get(key, "")
    
    @staticmethod
    def get_all_tooltips() -> dict:
        """
        Возвращает все подсказки.
        :return: Словарь всех подсказок
        """
        return TOOLTIPS.copy()


# Глобальный экземпляр
_tooltips_provider = None

def get_tooltips_provider():
    """Получает глобальный экземпляр поставщика подсказок"""
    global _tooltips_provider
    if _tooltips_provider is None:
        _tooltips_provider = TooltipsProvider()
    return _tooltips_provider
